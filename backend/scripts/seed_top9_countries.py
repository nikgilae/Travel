async def seed_top9():
    async with AsyncSessionLocal() as session:
        country_repo = CountryRepository(session)
        city_repo = CityRepository(session)

        for country_data in TOP9_COUNTRIES:
            existing = await country_repo.get_by_name(country_data["name"])
            if existing:
                await session.execute(
                    text("UPDATE countries SET description = :desc WHERE id = :id"),
                    {"desc": country_data["description"], "id": existing.id},
                )
                print(f"  ✏️  Обновлено описание: {country_data['name']}")
                country = existing
            else:
                country = await country_repo.create(
                    name=country_data["name"],
                    content=country_data["content"],
                )
                await session.execute(
                    text("UPDATE countries SET description = :desc WHERE id = :id"),
                    {"desc": country_data["description"], "id": country.id},
                )
                print(f"  ✅ Создана страна: {country_data['name']}")

            for city_data in country_data["cities"]:
                existing_city = await city_repo.get_by_name_and_country(
                    city_data["name"], country.id
                )
                if not existing_city:
                    await city_repo.create(
                        country_id=country.id,
                        name=city_data["name"],
                        content=city_data["content"],
                    )
                    print(f"     🏙  Добавлен город: {city_data['name']}")

        await session.commit()
        print("\n✅ Топ-9 стран заполнены!")


if __name__ == "__main__":
    asyncio.run(seed_top9())
