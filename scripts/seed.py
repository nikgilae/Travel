# ── СКРИПТ ────────────────────────────────────────────────────────────────────

async def seed():
    async with AsyncSessionLocal() as session:
        country_repo = CountryRepository(session)
        city_repo = CityRepository(session)
        poi_repo = POIRepository(session)
        rule_repo = RuleRepository(session)
        country_rule_repo = CountryRuleRepository(session)
        city_rule_repo = CityRuleRepository(session)
        poi_rule_repo = POIRuleRepository(session)

        try:
            # ── 1. СТРАНА ─────────────────────────────────────────────────────
            print("🌍 Создаём страну...")
            existing = await country_repo.get_by_name(COUNTRY["name"])
            if existing:
                print(f"  ⚠️  Страна '{COUNTRY['name']}' уже существует — пропускаем")
                country = existing
            else:
                country = await country_repo.create(**COUNTRY)
                print(f"  ✅ {country.name}")

            # ── 2. ПРАВИЛА СТРАНЫ ─────────────────────────────────────────────
            print("\n📋 Создаём правила страны...")
            for rule_data in COUNTRY_RULES:
                rule = await rule_repo.create(content=rule_data["content"])
                exists = await country_rule_repo.exists(country.id, rule.id)
                if not exists:
                    await country_rule_repo.create(
                        country_id=country.id,
                        rule_id=rule.id,
                        is_strict=rule_data["is_strict"],
                    )
                    print(f"  ✅ {rule_data['content'][:60]}...")

            await session.commit()
            print("  💾 Страна и правила сохранены")

            # ── 3. ГОРОДА ─────────────────────────────────────────────────────
            print("\n🏙  Создаём города...")
            city_map = {}
            for city_data in CITIES:
                existing_city = await city_repo.get_by_name_and_country(
                    city_data["name"], country.id
                )
                if existing_city:
                    print(f"  ⚠️  Город '{city_data['name']}' уже существует — пропускаем")
                    city_map[city_data["name"]] = existing_city
                else:
                    city = await city_repo.create(
                        country_id=country.id,
                        name=city_data["name"],
                        content=city_data["content"],
                    )
                    city_map[city_data["name"]] = city
                    print(f"  ✅ {city.name}")

            await session.commit()
            print("  💾 Города сохранены")

            # ── 4. ПРАВИЛА ГОРОДОВ ────────────────────────────────────────────
            print("\n📋 Создаём правила городов...")
            for city_name, rules in CITY_RULES.items():
                city = city_map.get(city_name)
                if not city:
                    print(f"  ❌ Город '{city_name}' не найден")
                    continue
                for rule_data in rules:
                    rule = await rule_repo.create(content=rule_data["content"])
                    exists = await city_rule_repo.exists(city.id, rule.id)
                    if not exists:
                        await city_rule_repo.create(
                            city_id=city.id,
                            rule_id=rule.id,
                            is_strict=rule_data["is_strict"],
                        )
                        print(f"  ✅ [{city_name}] {rule_data['content'][:55]}...")

            await session.commit()
            print("  💾 Правила городов сохранены")

            # ── 5. POI С ПРАВИЛАМИ ────────────────────────────────────────────
            print("\n📍 Создаём POI с правилами...")
            for poi_data in POIS:
                city = city_map.get(poi_data["city"])
                if not city:
                    print(f"  ❌ Город '{poi_data['city']}' не найден — пропускаем {poi_data['name']}")
                    continue

                geom = None
                if poi_data.get("lat") and poi_data.get("lon"):
                    geom = ST_SetSRID(ST_MakePoint(poi_data["lon"], poi_data["lat"]), 4326)

                poi = await poi_repo.create(
                    name=poi_data["name"],
                    description=poi_data["description"],
                    information=poi_data["information"],
                    geom=geom,
                    is_indoor=poi_data["is_indoor"],
                )
                print(f"  ✅ {poi.name} ({poi_data['city']})")

                for rule_data in poi_data.get("rules", []):
                    rule = await rule_repo.create(content=rule_data["content"])
                    exists = await poi_rule_repo.exists(poi.id, rule.id)
                    if not exists:
                        await poi_rule_repo.create(
                            poi_id=poi.id,
                            rule_id=rule.id,
                            is_strict=rule_data["is_strict"],
                        )
                        print(f"     📋 {rule_data['content'][:55]}...")

                await session.commit()

            print("\n✅ Все данные успешно загружены в БД!")

        except Exception as e:
            await session.rollback()
            print(f"\n❌ Ошибка при сидинге: {e}")
            raise


