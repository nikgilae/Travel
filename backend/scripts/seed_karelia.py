async def seed():
    async with AsyncSessionLocal() as session:
        poi_repo    = POIRepository(session)
        rule_repo   = RuleRepository(session)
        poi_rule_repo = POIRuleRepository(session)
        city_rule_repo = CityRuleRepository(session)

        # Find Karelia city
        result = await session.execute(
            select(City).where(City.name == CITY_NAME)
        )
        city = result.scalar_one_or_none()
        if not city:
            print(f"❌ Город '{CITY_NAME}' не найден в БД. Сначала создайте его через /onboarding.")
            return

        print(f"✅ Найден город: {city.name} ({city.id})")

        # Add city rules
        print("\n📋 Добавляем правила города...")
        for rule_data in CITY_RULES:
            rule = await rule_repo.create(content=rule_data["content"])
            exists = await city_rule_repo.exists(city.id, rule.id)
            if not exists:
                await city_rule_repo.create(
                    city_id=city.id,
                    rule_id=rule.id,
                    is_strict=rule_data["is_strict"],
                )
                print(f"  ✅ {rule_data['content'][:65]}...")
        await session.commit()

        # Add POIs
        print(f"\n📍 Добавляем POI для {city.name}...")
        for poi_data in POIS:
            geom = ST_SetSRID(ST_MakePoint(poi_data["lon"], poi_data["lat"]), 4326)

            poi = await poi_repo.create(
                name=poi_data["name"],
                description=poi_data["description"],
                information=poi_data["information"],
                geom=geom,
                is_indoor=poi_data["is_indoor"],
                city_id=city.id,
            )
            print(f"  ✅ {poi.name}")

            for rule_data in poi_data.get("rules", []):
                rule = await rule_repo.create(content=rule_data["content"])
                exists = await poi_rule_repo.exists(poi.id, rule.id)
                if not exists:
                    await poi_rule_repo.create(
                        poi_id=poi.id,
                        rule_id=rule.id,
                        is_strict=rule_data["is_strict"],
                    )

            await session.commit()

        print(f"\n✅ Добавлено {len(POIS)} POI для города {city.name}!")


if __name__ == "__main__":
    asyncio.run(seed())
